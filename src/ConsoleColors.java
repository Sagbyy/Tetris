public class ConsoleColors {
        // Class des couleurs pour la console

        // Reset
        public static final String RESET = "\033[0m";  // Text Reset

        // Colors
        public static final String RED = "\033[0;31m";   // RED (Exemple intro and Tetrimino Z)
        public static final String YELLOW_BRIGHT = "\033[0;33;1m";  // YELLOW (Tetris logo and Tetrimino O)
        public static final String YELLOW_BOLD = "\033[1;33m"; // YELLOW (Points)
        public static final String YELLOW = "\033[0;33m";  // YELLOW (Tetris logo and Tetrimino L)
        public static final String CYAN = "\033[0;36m";    // CYAN (Tetrimino I)
        public static final String PURPLE = "\033[0;35m";  // PURPLE (Tetrimino T)
        public static final String BLUE = "\033[0;34m";    // BLUE (Tetrimino J)
        public static final String GREEN = "\033[0;32m";   // GREEN (Tetrimino S)
}
